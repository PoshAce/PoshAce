class HideField extends \Magento\Framework\View\Element\Html\Select
{
    /**
     * @param string $value
     * @return Magently\Tutorial\Block\Adminhtml\Form\Field\Activation
     */
    public function setInputName($value)
    {
        return $this->setName($value);
    }

    /**
     * Parse to html.
     *
     * @return mixed
     */
    public function _toHtml()
    {
        if (!$this->getOptions()) {
            $this->addOption(0, __('No'));
            $this->addOption(1, __('Yes'));
        }

        return parent::_toHtml();
    }
}